package model;

public class Animal {

	private int id;
	private String type;
	private int age;

	public Animal(int id, String type, int age) {
		this.id = id;
		this.type = type;
		this.age = age;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
}
